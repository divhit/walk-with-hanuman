const DONE_KEY = "wwh_completed_chapters";
const PROGRESS_KEY = "wwh_chapter_progress";

export function completedChapters() {
  try {
    return JSON.parse(localStorage.getItem(DONE_KEY)) || [];
  } catch {
    return [];
  }
}

export function markChapterDone(key) {
  const done = new Set(completedChapters());
  done.add(key);
  localStorage.setItem(DONE_KEY, JSON.stringify([...done]));
  saveProgress(key, null);
}

export function chapterProgress() {
  try {
    return JSON.parse(localStorage.getItem(PROGRESS_KEY)) || {};
  } catch {
    return {};
  }
}

export function saveProgress(chapterKey, sceneId) {
  const p = chapterProgress();
  if (sceneId) p[chapterKey] = sceneId;
  else delete p[chapterKey];
  localStorage.setItem(PROGRESS_KEY, JSON.stringify(p));
}
